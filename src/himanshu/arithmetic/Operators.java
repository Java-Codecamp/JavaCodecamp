package himanshu.arithmetic;

public class Operators {

    public static void main(String[] args) {
        int a = 20, b = 2;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
    }
}
